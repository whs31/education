library ieee;
use ieee.std_logic_1164.all;

entity lab_5_1 is
    port ( 
        clk : in std_logic;
        clear : in std_logic;
        preset : in std_logic;
        j : in std_logic;
        k : in std_logic;
        e : in std_logic;
        jk_out : out std_logic
    );
end entity;

architecture impl of lab_5_1 is
    signal jk: std_logic;
begin
    process (clear, preset, clk) begin
        if clear = '1' 
            then jk <= '0';
        elsif preset = '1' 
            then jk <= '1';
        elsif rising_edge(clk) then
            if e = '1' then
                if k = '0' then
                    case j is
                        when '0' => jk <= '1'; 
                        when '1' => jk <= jk;
                    end case;
                end if;
                if k = '1' then
                    case j is
                        when '0' => jk <='0';
                        when '1' => jk <= not jk after 1 ns;
                    end case;
                end if;
            end if;
        end if;
    end process;
    jk_out <= jk;
end impl;
